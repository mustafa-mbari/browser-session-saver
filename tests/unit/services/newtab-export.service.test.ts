describe('importDashboardFromJSON', () => {
  it('returns an error for malformed JSON', async () => {
    const { importDashboardFromJSON } = await import('@core/services/newtab-export.service');

    const result = await importDashboardFromJSON('not valid json {{{');
    expect(result.success).toBe(false);
    expect(result.error).toMatch(/valid JSON/i);
  });

  it('returns an error when the file is not a dashboard export', async () => {
    const { importDashboardFromJSON } = await import('@core/services/newtab-export.service');

    const result = await importDashboardFromJSON(JSON.stringify({ sessions: [], version: '1.0.0' }));
    expect(result.success).toBe(false);
    expect(result.error).toMatch(/browser hub dashboard export/i);
  });

  it('returns success and counts on a valid import', async () => {
    const { importDashboardFromJSON } = await import('@core/services/newtab-export.service');

    const envelope = buildEnvelope([
      { id: 'b1', name: 'Main', icon: '', categoryIds: [], createdAt: '', updatedAt: '' },
    ]);
    const result = await importDashboardFromJSON(JSON.stringify(envelope));

    expect(result.success).toBe(true);
    expect(result.counts?.boards).toBe(1);
    expect(result.counts?.categories).toBe(0);
  });

  it('replaces existing boards with imported ones', async () => {
    const { importDashboardFromJSON } = await import('@core/services/newtab-export.service');

    await db.put('boards', { id: 'old', name: 'Old Board', icon: '', categoryIds: [], createdAt: '', updatedAt: '' });

    const envelope = buildEnvelope([
      { id: 'new', name: 'New Board', icon: '', categoryIds: [], createdAt: '', updatedAt: '' },
    ]);
    await importDashboardFromJSON(JSON.stringify(envelope));

    const boards = await db.getAll<{ id: string }>('boards');
    expect(boards).toHaveLength(1);
    expect(boards[0].id).toBe('new');
  });

  it('does not clear the wallpaperImages store', async () => {
    const { importDashboardFromJSON } = await import('@core/services/newtab-export.service');

    await db.putBlob('wp1', new Blob(['image-data']));

    await importDashboardFromJSON(JSON.stringify(buildEnvelope([])));

    const blob = await db.getBlob('wp1');
    expect(blob).not.toBeNull();
  });

  it('strips backgroundImageId and resets backgroundType when it was image', async () => {
    const { importDashboardFromJSON } = await import('@core/services/newtab-export.service');
    const { updateNewTabSettings } = await import('@core/services/newtab-settings.service');

    const envelope = buildEnvelope([], { backgroundType: 'image', backgroundImageId: 'wp-123' });
    await importDashboardFromJSON(JSON.stringify(envelope));

    const [settingsArg] = vi.mocked(updateNewTabSettings).mock.calls[0] as [Record<string, unknown>];
    expect(settingsArg['backgroundType']).toBe('gradient');
    expect(settingsArg).not.toHaveProperty('backgroundImageId');
  });

  it('preserves settings when backgroundType is not image', async () => {
    const { importDashboardFromJSON } = await import('@core/services/newtab-export.service');
    const { updateNewTabSettings } = await import('@core/services/newtab-settings.service');

    const envelope = buildEnvelope([], { backgroundType: 'gradient', theme: 'dark' });
    await importDashboardFromJSON(JSON.stringify(envelope));

    const [settingsArg] = vi.mocked(updateNewTabSettings).mock.calls[0] as [Record<string, unknown>];
    expect(settingsArg['backgroundType']).toBe('gradient');
    expect(settingsArg['theme']).toBe('dark');
  });

  it('handles an empty envelope without error', async () => {
    const { importDashboardFromJSON } = await import('@core/services/newtab-export.service');

    const result = await importDashboardFromJSON(JSON.stringify(buildEnvelope([])));
    expect(result.success).toBe(true);
    expect(await db.getAll('boards')).toHaveLength(0);
  });
});
